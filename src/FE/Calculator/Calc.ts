namespace FE.Calculator {

    export class Calc {

        /**
         *
         * @param left
         * @param right
         * @returns {number}
         */
        public add(left:number, right:number) {
            return left + right;
        }

        /**
         *
         * @param left
         * @param right
         * @returns {number}
         */
        public sub(left:number, right:number) {
            return left - right;
        }
    }
}
